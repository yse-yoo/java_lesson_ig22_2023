package sample;

public class ItemRepeat {
    
    public static void main(String[] args) {
        
        // コードアシスタント: fori
        // 0 - 9
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // 1 - 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }

}
